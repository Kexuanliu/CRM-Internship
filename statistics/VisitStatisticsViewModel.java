package com.xuebei.crm.statistics;

public class VisitStatisticsViewModel {

    // 接待日期
    private String receiveDate;

    // 员工
    private String employee;

    // 拜访类型
    private String visitType;

    // 拜访深浅
    private String visitDepth;

    // 外出
    private String out;

    // 院校
    private String college;

    // 院校ID
    private String collegeId;

    // 二级学院
    private String subDept;

    // 联系人
    private String contact;

    // 联系人ID
    private String contactId;

    // 联系方式
    private String mobile;

    // 拜访事由
    private String visitReason;

    // 拜访过程及结果
    private String visitProcessAndResult;

    public String getReceiveDate() { return receiveDate; }

    public void setReceiveDate(String receiveDate) { this.receiveDate = receiveDate; }

    public String getEmployee() { return employee; }

    public void setEmployee(String employee) { this.employee = employee;}

    public String getVisitType() { return visitType; }

    public void setVisitType(String visitType) { this.visitType = visitType; }

    public String getVisitDepth() { return visitDepth; }

    public void setVisitDepth(String visitDepth) { this.visitDepth = visitDepth; }

    public String getOut() { return out; }

    public void setOut(String out) { this.out = out; }

    public String getCollege() { return college; }

    public void setCollege(String college) { this.college = college; }

    public String getCollegeId() { return collegeId; }

    public void setCollegeId(String collegeId) { this.collegeId = collegeId; }

    public String getSubDept() { return subDept; }

    public void setSubDept(String subDept) { this.subDept = subDept; }

    public String getContact() { return contact; }

    public void setContact(String contact) { this.contact = contact; }

    public String getContactId() { return contactId; }

    public void setContactId(String contactId) { this.contactId = contactId; }

    public String getMobile() { return mobile; }

    public void setMobile(String mobile) { this.mobile = mobile; }

    public String getVisitReason() { return visitReason; }

    public void setVisitReason(String visitReason) { this.visitReason = visitReason; }

    public String getVisitProcessAndResult() { return visitProcessAndResult; }

    public void setVisitProcessAndResult(String visitProcessAndResult) { this.visitProcessAndResult = visitProcessAndResult; }
}
